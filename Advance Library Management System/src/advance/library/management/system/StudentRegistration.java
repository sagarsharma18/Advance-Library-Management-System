    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // save button
        if(txtid.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All fields are required.");
            txtid.requestFocus();//to get focus on this field after pop up
        }
        else if(txtname.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All fields are required.");
            txtname.requestFocus();//to get focus on this field after pop up
        }
        else if(txtcourse.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All fields are required.");
            txtcourse.requestFocus();//to get focus on this field after pop up
        }else if(txtbranch.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All fields are required.");
            txtbranch.requestFocus();//to get focus on this field after pop up
        } 
        else if(txtsemester.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All fields are required.");
            txtsemester.requestFocus();//to get focus on this field after pop up
        }
        else{
            // when all fields are  not empty then save this data
            Connection con=connect.Connection();//to establish connection with database by connect file
            try {
                PreparedStatement pst=con.prepareStatement("INSERT INTO STUDENT(id,name,course,branch,semester)values(?,?,?,?,?)");
                pst.setString(1, txtid.getText());//now to get data in databse table fields
                pst.setString(2, txtname.getText());
                pst.setString(3, txtcourse.getText());
                pst.setString(4, txtbranch.getText());
                pst.setString(5, txtsemester.getText());
                //to execute  this query and update data
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Information archived");
                //now after the data saved we need to empty again all fields
                txtid.setText("");
                txtname.setText("");
                txtcourse.setText("");
                txtbranch.setText("");
                txtsemester.setText("");
        
            } catch (SQLException ex) {
                Logger.getLogger(StudentRegistration.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_btnsaveActionPerformed

